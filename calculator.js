// retorna o valor da divisão entre os dois parâmetros
function div(v1, v2) {
    return v1/v2
}

console.log(div(4, 2));

// retorna o valor da subtração entre os dois parâmetros
function sub(v1, v2) {
    return v1 - v2;
}

console.log(sub(5, 3));

function mult(v1, v2) {
    return v1 * v2
}

console.log(mult(2,3))


function sum(v1, v2) {
    return v1 + v2
}

console.log(sum(5, 10));

